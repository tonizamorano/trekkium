function svg_shortcode_template($atts, $svg_content) {
    $atts = shortcode_atts(array(
        'class' => '', // Clase CSS opcional
        'width' => '', // Ancho opcional
        'height' => '', // Alto opcional
        'fill' => 'currentColor', // Color por defecto
    ), $atts, 'svg_icon');

    $class = $atts['class'] ? 'class="'.esc_attr($atts['class']).'"' : '';
    $width = $atts['width'] ? 'width="'.esc_attr($atts['width']).'"' : '';
    $height = $atts['height'] ? 'height="'.esc_attr($atts['height']).'"' : '';
    
    // Si se especifica fill, lo agregamos al tag SVG principal
    $fill_attr = $atts['fill'] ? 'fill="'.esc_attr($atts['fill']).'"' : '';
    
    // No modificar el contenido del SVG
    return '<svg '.$class.' '.$width.' '.$height.' '.$fill_attr.' aria-hidden="true">'.$svg_content.'</svg>';
}

